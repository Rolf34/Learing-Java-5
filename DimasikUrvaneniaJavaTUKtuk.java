public class DimasikUrvaneniaJavaTUKtuk {
    public static void main(String[] args) {
        double a = 15.123;
        double b = 9.563;
        double z = 0.717;

        double x = Math.pow(b, 0.25) + Math.cbrt(a - 1);

        double y = Math.abs(a - b * (Math.pow(Math.sin(z), 2) + Math.tan(z)));

        System.out.printf("x = %.4f%n", x);
        System.out.printf("y = %.4f%n", y);
    }
}
